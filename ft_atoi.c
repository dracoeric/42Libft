int	ft_atoi(const char *str)
{
	int nb;
	int i;
	int sgn;

	sgn = 1;
	i = 0;
	nb = 0;
	while (str[i] >= 1 && str[i] <= 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sgn = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = (nb * 10) + (int)str[i] - 48;
		i++;
	}
	return (sgn * nb);
}
